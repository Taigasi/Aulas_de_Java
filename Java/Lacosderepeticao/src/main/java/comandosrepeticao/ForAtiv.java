package comandosrepeticao;

public class ForAtiv {
    public static void main(String[] args) {

        for (int i = 0; i <= 200; i+= 2) {
            System.out.println(i); // i++ ele incrementa de um em um e o i+ incrementa de 2 em dois
        } // no caso desse código, ele tem que começar do zero para contabilizar os números pares de 2 em 2
    }
}
