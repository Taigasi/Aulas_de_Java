public class SegundoExercicio {

    public static void main(String[] args) {

        String nome = "Débora"; // nome de classe sempre é maiuscula
        int idade = 52;  // int é utilizado para valores inteiros
        float altura = 1.78f; // Floot é utilizado para número pequenos pós virgula e tem que ser identificado com f no final
        double salario = 52000; // Double é utilizado para números maiores pós virgula

        System.out.println("Meu nome é:" + nome);
        System.out.println("Minha idade é:" + idade);
        System.out.println("Minha altura é:" + altura);
        System.out.println("Meu salário é: " + salario);

    }
}
